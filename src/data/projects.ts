/** Скриншот: либо просто строка-путь, либо объект с подписями.
 * `label` — короткий моно-лейбл под плиткой (a-la Kardio: «SPLASH SCREEN»).
 * `caption` — полное описание, видимое в overlay-просмотре.
 * `protected` — закрытый по NDA скрин: рендерится в блюре до ввода пароля.
 * `kind` — `"video"` для MP4-демок (autoplay/muted/loop в плитке, controls в overlay).
 *           Default — `"image"`.
 * `poster` — постер-картинка для видео (отображается до загрузки/при паузе).
 */
export type Screenshot =
  | string
  | {
      src: string;
      label?: string;
      caption?: string;
      alt?: string;
      protected?: boolean;
      kind?: "image" | "video";
      poster?: string;
    };

export interface Project {
  slug: string;
  title: string;
  company: string;
  role: string;
  period: string;
  year: number;
  description: string;
  longDescription: string;
  tags: string[];
  metric?: string;
  metricLabel?: string;
  type: "work" | "experiment";
  coverColor: string;
  coverImage?: string;
  /**
   * Optional cover video (mp4/webm). Если задан — карточка показывает автоплей-видео
   * вместо статичного coverImage. coverImage при этом служит постером для <video>.
   */
  coverVideo?: string;
  /**
   * Опционально: timestamp в секундах, на котором держится «холодный» кадр.
   * — На hero страницы кейса видео ставится на эту секунду и стоит на паузе.
   * — На карточке главной: hover → play 0..pauseAt → pause; mouseLeave → play pauseAt..end → pause на конце.
   */
  coverVideoPauseAt?: number;
  /**
   * Опционально: статичная картинка только для hero страницы кейса.
   * Если задана — перебивает coverVideo на странице кейса (на главной карточка остаётся с видео).
   */
  heroImage?: string;
  screenshots?: Screenshot[];
  /**
   * Режим раскладки top-level скринов и default для секций.
   * - `"web"` (default) — 2 столбца, 16:9, под десктопные веб-интерфейсы.
   * - `"phone"` — до 5 столбцов, портретный iPhone-aspect, под мобильные скрины (Kardio-style).
   */
  screenshotsMode?: "web" | "phone";
  sections?: {
    title: string;
    /** Старый формат — single paragraph. Используется в кейсах, где ещё не разнесли на структурные блоки. */
    content?: string;
    /** Новый структурный формат: задача → подход → что способствовало → результат. */
    context?: string;
    approach?: string;
    helped?: string;
    result?: string;
    /** Мини-цифры внутри секции — рендерятся сеткой 2×N. */
    callouts?: { value: string; label: string }[];
    /** Inline-пруфы: ссылки рядом с релевантным контентом, а не только в нижнем блоке. */
    links?: { label: string; url: string; thumbnail?: string; kind?: "video" | "article" | "site" | "github" }[];
    /**
     * Hero-плакаты секции. Рендерятся БЕЗ рамки телефона, отдельным блоком над скриншотами.
     * Если 1 элемент — full-width 16:9, если 2-4 — grid.
     */
    heroes?: { src: string; alt?: string; aspect?: string }[];
    screenshots?: Screenshot[];
    /** Override `screenshotsMode` проекта для этой секции. */
    screenshotsMode?: "web" | "phone";
    /**
     * Опционально: горизонтальный таймлайн событий внутри секции.
     * Рендерится между текстом и скриншотами как графическая шкала с датами.
     */
    timeline?: { date: string; title: string; note?: string }[];
  }[];
  results?: {
    value: string;
    label: string;
  }[];
  links?: {
    label: string;
    url: string;
    category?: string;
    /** Опционально: путь к JPG-превью (1280x720). Для видео-ссылок будет нарисован play-иконкой поверх. */
    thumbnail?: string;
    /** Опционально: тип ссылки. Если "video" — рендерится с play-иконкой. */
    kind?: "video" | "article" | "site" | "github";
  }[];
}

